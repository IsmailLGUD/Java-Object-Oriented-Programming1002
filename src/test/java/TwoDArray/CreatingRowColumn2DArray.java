package TwoDArray;

public class CreatingRowColumn2DArray {

	public static void main(String[] args) {
		int[][] semeseter = new int[2][3];
		semeseter[0][0] = 1;
		semeseter[0][1] = 1;
		semeseter[0][2] = 1;
		semeseter[1][0] = 1;
		semeseter[1][1] = 1;
		semeseter[1][2] = 1;

		System.out.println(semeseter[0][0]);
		System.out.println(semeseter[0][1]);
		System.out.println(semeseter[0][2]);
		System.out.println(semeseter[1][0]);
		System.out.println(semeseter[1][1]);
		System.out.println(semeseter[1][2]);

	}

}
